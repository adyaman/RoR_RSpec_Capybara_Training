require "rails_helper"

RSpec.feature "Listing articles" do

  before do
    @article1 = Article.create(title: "The first article", body: "Compassion Love Hosgoru")
    @article2 = Article.create(title: "The second article", body: "Peace Empathy Justice")
  end
  scenario "a user lists all articles" do
    visit "/"

    expect(page).to have_content(@article1.title)
    expect(page).to have_content(@article1.body)
    expect(page).to have_content(@article2.title)
    expect(page).to have_content(@article2.body)
    expect(page).to have_link(@article1.title)
    expect(page).to have_link(@article2.title)

  end
  scenario "No articles available" do
    Article.delete_all
    visit "/"

    expect(page).not_to have_content(@article1.title)
    expect(page).not_to have_content(@article1.body)
    expect(page).not_to have_content(@article2.title)
    expect(page).not_to have_content(@article2.body)
    expect(page).not_to have_link(@article1.title)
    expect(page).not_to have_link(@article2.title)

    within("h1#no-articles") do
      expect(page).to have_content("No articles created yet")
    end

  end

end